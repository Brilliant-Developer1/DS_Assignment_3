using namespace std;
class Node
{
public:
    int val;
    Node *next;

    Node(int val)
    {
        this->val = val;
        this->next = NULL;
    }
};

class myStack
{
public:
    vector<int> v;
    void push(int value)
    {
        v.push_back(value);
    }
    void pop()
    {
        v.pop_back();
    }
    int top()
    {
        return v.back();
    }
    int size()
    {
        return v.size();
    }
    bool empty()
    {
        if (v.size() == 0)
            return true;
        else
            return false;
    }
};

class myQueue
{
public:
    Node *head = NULL;
    Node *tail = NULL;
    int sz = 0;
    void push(int value)
    {
        sz++;
        Node *newNode = new Node(value);

        if (head == NULL)
        {
            head = newNode;
            tail = newNode;
            return;
        }
        tail->next = newNode;
        tail = tail->next;
    }
    void pop()
    {
        sz--;
        Node *deleteNode = head;
        head = head->next;
        delete deleteNode;
        if (head == NULL)
        {
            tail = NULL;
        }
    }

    int front()
    {
        return head->val;
    }
    int size()
    {
        return sz;
    }
    bool empty()
    {
        if (sz == 0)
            return true;
        else
            return false;
    }
};
int main()
{
    int N, M;
    cin >> N >> M;
    // Take input of Stack and Queue
    myStack st;
    myQueue que;
    for (int i = 0; i < N; i++)
    {
        int x;
        cin >> x;
        st.push(x);
    }

    for (int i = 0; i < M; i++)
    {
        int x;
        cin >> x;
        que.push(x);
    }
    // Transfer values to list
    list<int> stList;
    list<int> queList;
    while (!st.empty())
    {
        stList.push_back(st.top());
        st.pop();
    }
    while (!que.empty())
    {
        queList.push_back(que.front());
        que.pop();
    }
    // check the lists
    if (stList == queList)
    {
        cout << "YES";
    }
    else
    {
        cout << "NO";
    }

    return 0;
}